float get_pressure_from_temp(int16_t temperature_index, int8_t gas_selected) {
  int16_t actual_index;
  switch (gas_selected) {

    case 0:
      actual_index = temperature_index + 41;
      if (actual_index >= 0 && actual_index < (sizeof(R22_Pressure) / sizeof(R22_Pressure[0]))) {
        float pressure = R22_Pressure[actual_index];
        return pressure;
      } else {
        return -1;
      }
      break;

    case 2:
      actual_index = temperature_index;
      if (actual_index >= 0 && actual_index < (sizeof(R22_Pressure) / sizeof(r410_liquid_PSI[0]))) {
        float pressure = r410_liquid_PSI[actual_index];
        return pressure;
      } else {
        return -1;
      }
      break;

    case 4:
      actual_index = temperature_index;
      if (actual_index >= 0 && actual_index < (sizeof(R22_Pressure) / sizeof(r407_liquid_PSI[0]))) {
        float pressure = r407_liquid_PSI[actual_index];
        return pressure;
      } else {
        return -1;
      }
      break;

    case 7:
      actual_index = temperature_index;
      if (actual_index >= 0 && actual_index < (sizeof(R22_Pressure) / sizeof(r410_vapor_PSI[0]))) {
        float pressure = r410_vapor_PSI[actual_index];
        return pressure;
      } else {
        return -1;
      }
      break;

    case 9:
      actual_index = temperature_index;
      if (actual_index >= 0 && actual_index < (sizeof(R22_Pressure) / sizeof(r407_vapor_PSI[0]))) {
        float pressure = r407_vapor_PSI[actual_index];
        return pressure;
      } else {
        return -1;
      }
      break;





    default:  // R22 Gas
      actual_index = temperature_index + 41;
      if (actual_index >= 0 && actual_index < (sizeof(R22_Pressure) / sizeof(R22_Pressure[0]))) {
        float pressure = R22_Pressure[actual_index];
        return pressure;
      } else {
        return -1;
      }
      break;
  }
}
